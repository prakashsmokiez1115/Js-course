// Addition
console.log(10 + 20);   // 30

// Subtraction
console.log(20 - 5);    // 15

// Multiplication
console.log(20 * 2);    // 40

// Division
console.log(20 / 2);    // 10  ‚Üí Quotient

// Modulus (Remainder)
console.log(21 % 5);    // 1

// Exponentiation (Power)
console.log(3 ** 4);    // 81



// ===================================================
// 2Ô∏è‚É£ Increment Operator ( ++ )
// ===================================================

// Manual increment
let num = 10;
num = num + 1;          // 10 + 1 = 11
console.log(num);


// Post Increment
let numPost = 15;
numPost++;              // 15 ‚Üí 16
console.log(numPost);


// Pre Increment
++numPost;              // 16 ‚Üí 17
console.log(numPost);



// ===================================================
// 3Ô∏è‚É£ Decrement Operator ( -- )
// ===================================================

// Post Decrement
let num1 = 20;
num1--;                 // 20 ‚Üí 19
console.log(num1);


// Pre Decrement
--num1;                 // 19 ‚Üí 18
console.log(num1);
























